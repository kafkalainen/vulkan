#include "../libkaf.h"

namespace kaf_math
{
	double math::add(double a, double b)
	{
		return (a + b);
	}
	float math::add(float a, float b)
	{
		return (a + b);
	}
	int math::add(int a, int b)
	{
		return (a + b);
	}
	double math::dec(double a, double b)
	{
		return (a - b);
	}
	float math::dec(float a, float b)
	{
		return (a - b);
	}
	int math::dec(int a, int b)
	{
		return (a - b);
	}
	double math::mul(double a, double b)
	{
		return (a * b);
	}
	float math::mul(float a, float b)
	{
		return (a * b);
	}
	int math::mul(int a, int b)
	{
		return (a * b);
	}
	double math::div(double a, double b)
	{
		return (a / b);
	}
	float math::div(float a, float b)
	{
		return (a / b);
	}
	int math::div(int a, int b)
	{
		return (a / b);
	}
	int math::mod(int a, int b)
	{
		return (a % b);
	}
}
